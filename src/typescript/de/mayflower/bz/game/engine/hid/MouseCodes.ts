/** ********************************************************************************************************************
*   All mouse codes that are used by the mouse system.
***********************************************************************************************************************/
export abstract class MouseCodes
{
    /** Button ID for left mouse button. */
    public      static  readonly    MOUSE_BUTTON_LEFT           :number                 = 0;
    /** Button ID for center mouse button. */
    public      static  readonly    MOUSE_BUTTON_CENTER         :number                 = 1;
    /** Button ID for right mouse button. */
    public      static  readonly    MOUSE_BUTTON_RIGHT          :number                 = 2;
}
