/** ********************************************************************************************************************
*   Represents one GUI action being launched on performing a menu item.
***********************************************************************************************************************/
export enum GUIAction
{
    /** Resume to the game screen. */
    RESUME_GAME,

    /** Switch to stage 1. */
    SWITCH_TO_STAGE_1,
    /** Switch to stage 2. */
    SWITCH_TO_STAGE_2,
}
