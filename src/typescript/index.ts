import { WebApp } from './bz/base/WebApp';

/** ********************************************************************************************************************
*   Being invoked when the website is loaded completely.
***********************************************************************************************************************/
window.onload = () : void  =>
{
    const webApp :WebApp = new WebApp();
    webApp.start();
};

/** ********************************************************************************************************************
*   Being invoked when the website is left.
***********************************************************************************************************************/
window.onunload = () : void  =>
{
    // no actions required
};
